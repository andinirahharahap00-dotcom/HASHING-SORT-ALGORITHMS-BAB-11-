# ==================================
# Fungsi Quick Sort
# ==================================

# def digunakan untuk membuat fungsi.
# Fungsi ini bernama quick_sort dan menerima data berupa list.
def quick_sort(data):

    # len() adalah fungsi bawaan Python untuk menghitung jumlah elemen dalam list.
    # Jika jumlah data 0 atau 1, data sudah pasti urut.
    if len(data) <= 1:
        return data

    # // adalah pembagian bulat.
    # len(data)//2 mencari posisi tengah list.
    # Nilai tengah dipakai sebagai pivot (nilai pembanding).
    pivot = data[len(data) // 2]

    # Membuat list kosong untuk menampung hasil pemisahan data.
    left = []     # nilai yang lebih kecil dari pivot
    middle = []   # nilai yang sama dengan pivot
    right = []    # nilai yang lebih besar dari pivot


    # for digunakan untuk melakukan perulangan.
    # item akan mengambil nilai dari data satu per satu.
    for item in data:

        # Jika item lebih kecil dari pivot,
        # masukkan ke list left.
        if item < pivot:
            # append() adalah fungsi list Python untuk menambahkan data ke akhir list.
            left.append(item)


        # Jika item sama dengan pivot,
        # masukkan ke list middle.
        elif item == pivot:
            middle.append(item)


        # Jika item lebih besar dari pivot,
        # masukkan ke list right.
        else:
            right.append(item)


    # Fungsi quick_sort dipanggil lagi untuk mengurutkan
    # bagian kiri dan kanan (rekursi).
    # Tanda + digunakan untuk menggabungkan list.
    return quick_sort(left) + middle + quick_sort(right)



