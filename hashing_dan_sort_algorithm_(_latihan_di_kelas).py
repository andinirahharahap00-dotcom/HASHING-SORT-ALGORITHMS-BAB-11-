def quick_sort(data, key):
    # Fungsi Quick Sort untuk mengurutkan data
    # key menentukan atribut yang digunakan untuk sorting


    if len(data) <= 1:
        # len() menghitung jumlah data
        # Jika data hanya 1 atau kosong, data sudah urut


        return data



    pivot = getattr(data[len(data)//2], key)
    # getattr() mengambil nilai atribut dari objek
    #
    # len(data)//2 mengambil posisi tengah data
    # Contoh key = "terjual"
    # maka mengambil nilai penjualan produk tengah



    left = []
    # Menyimpan data yang nilainya lebih besar dari pivot


    middle = []
    # Menyimpan data yang nilainya sama dengan pivot


    right = []
    # Menyimpan data yang nilainya lebih kecil dari pivot



    for item in data:
        # Mengulang setiap produk dalam data


        nilai = getattr(item, key)
        # Mengambil nilai atribut sesuai key



        if nilai > pivot:
            # Jika nilai lebih besar dari pivot


            left.append(item)
            # Memasukkan data ke bagian kiri



        elif nilai == pivot:
            # Jika nilai sama dengan pivot


            middle.append(item)
            # Memasukkan data ke bagian tengah



        else:

            right.append(item)
            # Memasukkan data ke bagian kanan



    return quick_sort(left, key) + middle + quick_sort(right, key)
    # Mengurutkan bagian kiri dan kanan
    # lalu menggabungkan hasilnya



