class HashTable:
    # Constructor, dijalankan saat objek dibuat
    def __init__(self, size):
        # Menyimpan ukuran hash table
        self.size = size

        # Membuat list berisi None sebanyak size
        # None menandakan slot masih kosong
        self.table = [None] * size

    # Fungsi hash menggunakan modulo (%)
    def hash_function(self, key):
        # Mengembalikan hasil key % ukuran tabel
        # return digunakan untuk mengembalikan nilai dari fungsi
        return key % self.size

    # Method untuk memasukkan data ke hash table
    def insert(self, key):
        # Menentukan index awal menggunakan hash function
        index = self.hash_function(key)

        # Selama slot belum kosong (bukan None)
        # maka cari slot berikutnya (Linear Probing)
        while self.table[index] is not None:
            # Geser ke index berikutnya
            # % self.size agar jika sampai index terakhir,
            # kembali ke index 0
            index = (index + 1) % self.size

        # Menyimpan data pada index yang ditemukan
        self.table[index] = key

    # Method untuk menampilkan isi hash table
    def display(self):
        # \n berarti pindah ke baris baru
        print("\nIsi Hash Table")

        # Mengulang karakter "-" sebanyak 30 kali
        print("-" * 30)

        # range(self.size) menghasilkan angka
        # dari 0 sampai self.size - 1
        for i in range(self.size):

            # Mengecek apakah slot kosong
            if self.table[i] is None:

                # f di depan string disebut f-string
                # digunakan untuk memasukkan nilai variabel ke dalam string
                # {i} akan diganti dengan nilai variabel i
                print(f"Index {i} : Empty")

            else:
                # {i} diganti index saat ini
                # {self.table[i]} diganti isi data pada index tersebut
                print(f"Index {i} : {self.table[i]}")


