class HashTable:
    # Membuat class HashTable untuk menyimpan data produk.

    def __init__(self, size):

        self.size = size
        # Menyimpan ukuran hash table.


        self.table = [[] for _ in range(size)]
        # Membuat hash table berupa list kosong.
        # Setiap index memiliki list untuk menyimpan data jika terjadi collision.



    def hash_function(self, key):
        # Fungsi hash untuk menentukan posisi data.

        return key % self.size
        # Menggunakan modulo untuk menentukan index.
        # Contoh: 101 % 7 = 3, jadi masuk index 3.



    def insert(self, product):
        # Fungsi untuk memasukkan data produk ke hash table.

        index = self.hash_function(product.code)
        # Menghitung index berdasarkan kode produk.


        self.table[index].append(product)
        # append() menambahkan produk ke dalam list pada index tersebut.
        # Jika index sudah ada isi, data tetap ditambahkan (chaining).



    def search(self, code):
        # Fungsi untuk mencari produk berdasarkan kode.
        # Bagian ini adalah kode pencarian.


        index = self.hash_function(code)
        # Mencari lokasi index berdasarkan kode yang dicari.


        bucket = self.table[index]
        # Mengambil isi list pada index tersebut.


        for product in bucket:
            # Mengecek setiap produk di dalam index tersebut.

            if product.code == code:
                # Membandingkan kode produk dengan kode yang dicari.

                return product
                # Mengembalikan produk jika ditemukan.


        return None
        # Jika tidak ditemukan, mengembalikan None.



    def display(self):
        # Fungsi untuk menampilkan isi hash table.


        print("\nIsi Hash Table")

        print("--------------------------------")


        for i in range(self.size):
            # Mengecek setiap index pada hash table.


            print(f"Index {i} :", end=" ")
            # f-string digunakan agar nilai i bisa masuk ke teks.
            # end=" " membuat output tetap di baris yang sama.


            if len(self.table[i]) == 0:
                # len() menghitung jumlah isi list.
                # Jika 0 berarti index kosong.

                print("Empty")


            else:

                for product in self.table[i]:
                    # Mengambil setiap produk di dalam index tersebut.


                    print(f"[{product.code}-{product.name}]", end=" ")
                    # Menampilkan kode dan nama produk.


                print()
                # Pindah ke baris baru.



