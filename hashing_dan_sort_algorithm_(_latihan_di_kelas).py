# ==========================================

def bubble_sort(data):
    # Membuat fungsi bubble_sort.
    # Fungsi ini menerima data berupa list untuk diurutkan.

    arr = data.copy()
    # copy() digunakan untuk membuat salinan data.
    # Agar data asli tidak berubah saat proses sorting.

    comparison = 0
    # Menyimpan jumlah perbandingan angka.

    swap = 0
    # Menyimpan jumlah pertukaran posisi angka.

    n = len(arr)
    # len() digunakan untuk menghitung jumlah elemen dalam list.
    # Nilainya disimpan ke variabel n.


    for i in range(n):
        # for digunakan untuk perulangan.
        # range(n) mengulang sebanyak jumlah data.


        for j in range(0, n - i - 1):
            # Perulangan untuk membandingkan angka yang bersebelahan.
            # n-i-1 mengurangi pengecekan karena bagian belakang sudah urut.


            comparison += 1
            # Menambah jumlah perbandingan sebanyak 1.


            if arr[j] > arr[j + 1]:
                # Mengecek apakah angka kiri lebih besar dari kanan.
                # Jika benar, posisi angka harus ditukar.


                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                # Menukar posisi dua angka.


                swap += 1
                # Menambah jumlah pertukaran.


    return arr, comparison, swap
    # Mengembalikan hasil:
    # 1. Data yang sudah urut
    # 2. Jumlah perbandingan
    # 3. Jumlah swap



