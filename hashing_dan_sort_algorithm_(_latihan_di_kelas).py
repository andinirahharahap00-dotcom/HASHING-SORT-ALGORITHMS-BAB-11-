def merge_sort(arr):
    # Membuat fungsi merge_sort.
    # Digunakan untuk mengurutkan data dengan Merge Sort.


    global comparison_merge
    global swap_merge
    # global digunakan agar variabel luar fungsi
    # bisa diubah dari dalam fungsi.


    if len(arr) <= 1:
        return arr
    # len() menghitung jumlah data.
    # Jika data hanya 1 atau kosong, langsung dikembalikan
    # karena sudah dianggap urut.


    mid = len(arr) // 2
    # // adalah pembagian bulat.
    # Digunakan untuk mencari posisi tengah data.


    left = merge_sort(arr[:mid])
    # arr[:mid] mengambil bagian kiri list.
    # Lalu bagian kiri diurutkan lagi.


    right = merge_sort(arr[mid:])
    # arr[mid:] mengambil bagian kanan list.
    # Lalu bagian kanan diurutkan lagi.


    return merge(left, right)
    # Menggabungkan bagian kiri dan kanan
    # yang sudah diurutkan.



def merge(left, right):
    # Fungsi untuk menggabungkan dua list yang sudah urut.


    global comparison_merge
    global swap_merge
    # Mengakses variabel penghitung dari luar fungsi.


    result = []
    # Membuat list kosong untuk menyimpan hasil gabungan.


    i = 0
    # Index untuk membaca data bagian kiri.


    j = 0
    # Index untuk membaca data bagian kanan.



    while i < len(left) and j < len(right):
        # while berjalan selama data kiri dan kanan masih ada.


        comparison_merge += 1
        # Menambah jumlah perbandingan.


        if left[i] < right[j]:
            # Membandingkan nilai kiri dan kanan.


            result.append(left[i])
            # append() digunakan untuk menambahkan data ke list hasil.


            i += 1
            # Berpindah ke data kiri berikutnya.


        else:

            result.append(right[j])
            # Jika kanan lebih kecil, masukkan nilai kanan.


            j += 1
            # Berpindah ke data kanan berikutnya.


        swap_merge += 1
        # Menghitung pemindahan data.



    while i < len(left):
        # Jika masih ada sisa data di kiri.


        result.append(left[i])
        # Tambahkan sisa data kiri.


        i += 1
        # Pindah ke data kiri berikutnya.



    while j < len(right):
        # Jika masih ada sisa data di kanan.


        result.append(right[j])
        # Tambahkan sisa data kanan.


        j += 1
        # Pindah ke data kanan berikutnya.



    return result
    # Mengembalikan hasil penggabungan.



