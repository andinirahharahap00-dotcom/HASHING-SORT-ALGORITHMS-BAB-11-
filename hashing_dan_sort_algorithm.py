# Fungsi untuk mengubah string menjadi nilai hash sederhana
def modASCII(string, cellNumber):

    total = 0

    # Menjumlahkan nilai ASCII setiap karakter
    for i in string:
        total += ord(i)

    # Hasil jumlah ASCII dibagi jumlah cell
    # lalu diambil sisanya
    return total % cellNumber


