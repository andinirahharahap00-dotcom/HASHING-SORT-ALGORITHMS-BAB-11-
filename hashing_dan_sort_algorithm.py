# Fungsi untuk menukar posisi dua elemen dalam list
def swap(my_list, index1, index2):
    my_list[index1], my_list[index2] = my_list[index2], my_list[index1]


# Menentukan posisi pivot yang sebenarnya
def pivot(my_list, pivot_index, end_index):

    # Menyimpan posisi pivot sementara
    swap_index = pivot_index

    # Mengecek semua elemen setelah pivot
    for i in range(pivot_index+1, end_index+1):

        # Jika ada nilai lebih kecil dari pivot
        if my_list[i] < my_list[pivot_index]:
            swap_index += 1

            # Pindahkan ke sebelah kiri pivot
            swap(my_list, swap_index, i)

    # Menempatkan pivot ke posisi yang benar
    swap(my_list, pivot_index, swap_index)

    return swap_index


# Fungsi utama quicksort (rekursif)
def quicksort_helper(my_list, left, right):

    # Berhenti jika bagian data tinggal 1 elemen
    if left < right:

        # Menentukan posisi pivot
        pivot_index = pivot(my_list, left, right)

        # Mengurutkan bagian kiri pivot
        quicksort_helper(my_list, left, pivot_index-1)

        # Mengurutkan bagian kanan pivot
        quicksort_helper(my_list, pivot_index+1, right)

    return my_list


# Memulai quicksort dari awal sampai akhir list
def quicksort(my_list):
    return quicksort_helper(my_list, 0, len(my_list)-1)


